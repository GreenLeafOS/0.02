#include "include/table.h"

TSS tss;
u16 tss_sel;

/************************************************************************/
/*					 		加载 tss
/*                            tss
/************************************************************************/
void tss_load(u16 sel)
{
	__asm volatile(
			"mov %0,%%ax	\n"
			"ltr %%ax"
			: : "g"(sel)
			);
}
